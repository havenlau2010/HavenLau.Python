import math

def math_fun_show():
    print("自然常数e====>math.e:",math.e)
    print("圆周率pi====>math.pi:",math.pi)
    print("对x向上取整，比如x=1.2，返回2====>math.ceil(1.4):",math.ceil(1.4))       # 对x向上取整，比如x=1.2，返回2

    print("对x向下取整，比如x=1.2，返回1====>math.floor(1.7):",math.floor(1.7))      # 对x向下取整，比如x=1.2，返回1

    print("指数运算，得到x的y次方====>math.pow(2,3):",math.pow(2,3))       # 指数运算，得到x的y次方

    #print("对数，默认基底为e。可以使用base参数，来改变对数的基地。比如math.log(100,base=10)====>math.log(100,base=10):",math.log(100,base=10))
    ## 对数，默认基底为e。可以使用base参数，来改变对数的基地。比如math.log(100,base=10)
    print("平方根====>math.sqrt(64):",math.sqrt(64))       # 平方根

